  student_id INTEGER,
  advisor_id INTEGER,
  FOREIGN KEY (student_id) REFERENCES student_info (student_id),
  FOREIGN KEY (advisor_id) REFERENCES professor_info (instructor_id)
);

INSERT INTO student_advisor (student_id, advisor_id)
SELECT s.student_id, p.instructor_id
FROM student_info s
JOIN professor_info p ON s.intended_major = p.department_id
WHERE p.instructor_id = (
  SELECT instructor_id
  FROM professor_info
  WHERE department_id = s.intended_major
  ORDER BY RANDOM()
  LIMIT 1
);

